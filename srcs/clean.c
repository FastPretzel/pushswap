#include "ps_h.h"

void	clean(t_malloc *tmc)
{
	int	i;

	i = -1;
	if (tmc->av)
		while (tmc->av[++i])
			free(tmc->av[i]);
	free(tmc->av);
	free(tmc->nums);
	ft_lstclear(&(tmc->a_st));
	exit(0);
}
